    public void consultar_cbx() {
        //Codigo que permite consultar registros en la base de datos
        try {
            Connection cn = DriverManager.getConnection(BD, Usuario, Clave);
            PreparedStatement pst = cn.prepareStatement("select nombre_facultad from facultades");

            ResultSet rs = pst.executeQuery();

            //llenar combobox para el comentaario
            cbx_facultad.addItem("Facultad");

            while (rs.next()) {
                cbx_facultad.addItem(rs.getString("nombre_facultad"));
            }

            rs.close();

        } catch (Exception e) {

        }
    }

    public void Tabla() {
        try {

            Connection cn = DriverManager.getConnection(BD, Usuario, Clave);

            PreparedStatement ps = cn.prepareStatement("select * from carreras");
            PreparedStatement ps2 = cn.prepareStatement("select * from carreras");
            //pstt4.setString(1, txt_buscar.getText().trim());

            ResultSet rs = ps.executeQuery();

            ResultSet rss = ps2.executeQuery();

            if (rs.next()) {

                DefaultTableModel modelo = new DefaultTableModel();

                modelo.addColumn("codigo_carrera");
                modelo.addColumn("nombre_carrera");
                modelo.addColumn("codigo_facultad");
                modelo.addColumn("estatus_carrera");

                tbl_carrera.setModel(modelo);

                String[] dato = new String[4];

                while (rss.next()) {
                    dato[0] = rss.getString(1);
                    dato[1] = rss.getString(2);
                    dato[2] = rss.getString(3);
                    dato[3] = rss.getString(4);
                    modelo.addRow(dato);

                }
            }

        } catch (Exception e) {

        }
    }


                tbl_carrera.setModel(modelo);

                String[] dato = new String[4];

                while (rss4.next()) {
                    dato[0] = rss4.getString(1);
                    dato[1] = rss4.getString(2);
                    dato[2] = rss4.getString(3);
                    dato[3] = rss4.getString(4);
                    modelo.addRow(dato);

                }

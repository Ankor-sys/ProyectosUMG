    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //Codigo que permite insertar registros en al base de datos
        try {
            Connection cn = DriverManager.getConnection(BD, Usuario, Clave);
            PreparedStatement pst = cn.prepareStatement("insert into carreras values(?,?,?,?)");

            pst.setString(1, txt_idcarrera.getText().trim());
            pst.setString(2, txt_nombre.getText().trim());
            pst.setString(3, lbl_codigofac.getText().trim());
            pst.setString(4, txt_estatus.getText().trim());

            pst.executeUpdate();

            txt_idcarrera.setText("");
            txt_nombre.setText("");
            lbl_codigofac.setText("");
            txt_estatus.setText("");

            //regresa ala primera opcion del combobox
            cbx_facultad.setSelectedIndex(0);

            JOptionPane.showMessageDialog(this, "¡REGISTRO EXITOSO!", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
            Tabla();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error en registro", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

